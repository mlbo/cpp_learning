void demoItem17GenerationRules() {
    SEP("条款17: 特殊成员函数生成规则");
    
    std::cout << "\n【1. 自动生成所有函数的类】\n";
    AutoGenerated a1;
    AutoGenerated a2 = a1;   // 拷贝构造（隐式生成）
    AutoGenerated a3 = std::move(a1);  // 移动构造（隐式生成）
    
    std::cout << "\n【2. Rule of Five 完整实现】\n";
    RuleOfFive r1;
    r1.setValue(100);
    std::cout << "r1 value: " << r1.value() << "\n";
    
    RuleOfFive r2 = r1;  // 拷贝构造
    std::cout << "r2 (copy): " << r2.value() << "\n";
    
    RuleOfFive r3 = std::move(r1);  // 移动构造
    std::cout << "r3 (move): " << r3.value() << "\n";
    std::cout << "r1 after move: " << r1.value() << " (已转移)\n";
    
    std::cout << "\n【3. Rule of Zero 最佳实践】\n";
    RuleOfZero z1(200);
    std::cout << "z1 value: " << z1.value() << "\n";
    
    // RuleOfZero z2 = z1;  // 错误！unique_ptr不可拷贝
    RuleOfZero z2 = std::move(z1);  // OK
    std::cout << "z2 (move): " << z2.value() << "\n";
    std::cout << "z1 after move: " << z1.value() << " (nullptr)\n";
}

void demoItem18UniquePtr() {
    SEP("条款18: unique_ptr资源管理");
    
    std::cout << "\n【1. 工厂函数模式】\n";
    auto stock = makeInvestment(InvestmentType::Stock);
    auto bond = makeInvestment(InvestmentType::Bond);
    
    std::cout << stock->name() << ", value: " << stock->value() << "\n";
    std::cout << bond->name() << ", value: " << bond->value() << "\n";
    
    std::cout << "\n【2. 容器中的unique_ptr】\n";
    std::vector<std::unique_ptr<Investment>> portfolio;
    portfolio.push_back(makeInvestment(InvestmentType::Stock));
    portfolio.push_back(makeInvestment(InvestmentType::Bond));
    
    std::cout << "Portfolio:\n";
    for (const auto& inv : portfolio) {
        std::cout << "  - " << inv->name() << "\n";
    }
    
    std::cout << "\n【3. Pimpl惯用法】\n";
    Widget w1("Widget1");
    w1.doSomething();
    
    Widget w2 = std::move(w1);  // 移动构造
    std::cout << "w2 name: " << w2.name() << "\n";
    
    std::cout << "\n【4. 自定义删除器】\n";
    {
        UniqueFile file(fopen("/tmp/test_day8.txt", "w"));
        if (file) {
            fprintf(file.get(), "Hello, unique_ptr!\n");
            LOG("写入文件成功");
        }
        // 离开作用域，FileDeleter自动关闭文件
    }
    LOG("文件已自动关闭");
}

