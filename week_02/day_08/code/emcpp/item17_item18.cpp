/**
 * @brief 投资基类
 */
class Investment {
public:
    virtual ~Investment() = default;
    virtual std::string name() const = 0;
    virtual double value() const = 0;
};

/**
 * @brief 股票投资
 */
class Stock : public Investment {
public:
    explicit Stock(const std::string& symbol, double price, int shares)
        : symbol_(symbol), price_(price), shares_(shares) {
        LOG("Stock 构造: " << symbol_);
    }
    
    ~Stock() override {
        LOG("Stock 析构: " << symbol_);
    }
    
    std::string name() const override { return "Stock: " + symbol_; }
    double value() const override { return price_ * shares_; }
    
private:
    std::string symbol_;
    double price_;
    int shares_;
};

/**
 * @brief 债券投资
 */
class Bond : public Investment {
public:
    explicit Bond(const std::string& issuer, double faceValue)
        : issuer_(issuer), faceValue_(faceValue) {
        LOG("Bond 构造: " << issuer_);
    }
    
    ~Bond() override {
        LOG("Bond 析构: " << issuer_);
    }
    
    std::string name() const override { return "Bond: " + issuer_; }
    double value() const override { return faceValue_; }
    
private:
    std::string issuer_;
    double faceValue_;
};

/**
 * @brief 投资类型枚举
 */
enum class InvestmentType { Stock, Bond };

/**
 * @brief 工厂函数：使用unique_ptr返回多态对象
 * 
 * 这是unique_ptr的经典应用场景：
 * - 返回指向基类的智能指针
 * - 自动管理派生类对象的生命周期
 * - 无需担心内存泄漏
 */
std::unique_ptr<Investment> makeInvestment(InvestmentType type) {
    switch (type) {
        case InvestmentType::Stock:
            return std::make_unique<Stock>("AAPL", 150.0, 100);
        case InvestmentType::Bond:
            return std::make_unique<Bond>("US Treasury", 10000.0);
    }
    return nullptr;
}

/**
 * @brief Pimpl惯用法演示
 * 
 * 使用unique_ptr隐藏实现细节
 */
// Widget.h 内容
class Widget {
public:
    Widget();
    explicit Widget(const std::string& name);
    ~Widget();  // 必须在实现文件中定义
    
    void doSomething();
    std::string name() const;
    
    // 拷贝操作需要自定义或删除（因为unique_ptr不可拷贝）
    Widget(const Widget&) = delete;
    Widget& operator=(const Widget&) = delete;
    
    // 移动操作可以显式默认
    Widget(Widget&&) noexcept = default;
    Widget& operator=(Widget&&) noexcept = default;
    
private:
    struct Impl;  // 前向声明
    std::unique_ptr<Impl> pImpl_;
};

// Widget.cpp 内容（在头文件中模拟）
struct Widget::Impl {
    std::string name;
    int value;
    
    Impl(const std::string& n) : name(n), value(42) {
        LOG("Widget::Impl 构造: " << name);
    }
    
    ~Impl() {
        LOG("Widget::Impl 析构: " << name);
    }
};

Widget::Widget() : pImpl_(std::make_unique<Impl>("default")) {}
Widget::Widget(const std::string& name) : pImpl_(std::make_unique<Impl>(name)) {}
Widget::~Widget() = default;  // 必须在能看到Impl完整定义的地方定义

void Widget::doSomething() {
    std::cout << "Widget '" << pImpl_->name << "' doing something (value=" << pImpl_->value << ")\n";
}

std::string Widget::name() const { return pImpl_->name; }

/**
 * @brief 自定义删除器演示
 */
struct FileDeleter {
    void operator()(FILE* f) const {
        if (f) {
            LOG("关闭文件");
            fclose(f);
        }
    }
};

using UniqueFile = std::unique_ptr<FILE, FileDeleter>;

