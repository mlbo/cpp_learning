/**
 * @brief 演示类：无任何显式声明
 * 
 * 编译器会自动生成所有特殊成员函数：
 * - 默认构造函数
 * - 析构函数
 * - 拷贝构造函数
 * - 拷贝赋值运算符
 * - 移动构造函数
 * - 移动赋值运算符
 */
class AutoGenerated {
public:
    AutoGenerated() { LOG("AutoGenerated 默认构造"); }
    // 其他函数由编译器自动生成
};

/**
 * @brief 演示类：显式声明析构函数
 * 
 * 声明析构函数后：
 * - 移动操作不再自动生成
 * - 拷贝操作仍然自动生成（但已废弃的行为）
 */
class WithDestructor {
public:
    WithDestructor() : data_(new int(0)) { LOG("WithDestructor 默认构造"); }
    
    ~WithDestructor() {
        LOG("WithDestructor 析构");
        delete data_;
    }
    
    // 拷贝操作会被隐式生成（但可能有问题！）
    // 移动操作不会被生成
    
private:
    int* data_;
};

/**
 * @brief 演示类：声明拷贝操作
 * 
 * 声明拷贝构造或拷贝赋值后：
 * - 移动操作不再自动生成
 */
class WithCopy {
public:
    WithCopy() : data_(new int(0)) { LOG("WithCopy 默认构造"); }
    
    // 拷贝构造
    WithCopy(const WithCopy& other) : data_(new int(*other.data_)) {
        LOG("WithCopy 拷贝构造");
    }
    
    // 拷贝赋值
    WithCopy& operator=(const WithCopy& other) {
        LOG("WithCopy 拷贝赋值");
        if (this != &other) {
            delete data_;
            data_ = new int(*other.data_);
        }
        return *this;
    }
    
    ~WithCopy() { delete data_; }
    
    // 移动操作不会自动生成
    
private:
    int* data_;
};

/**
 * @brief 演示类：声明移动操作
 * 
 * 声明移动操作后：
 * - 拷贝操作不再自动生成
 * - 默认构造函数不再自动生成
 */
class WithMove {
public:
    // 需要显式默认构造
    WithMove() : data_(new int(0)) { LOG("WithMove 默认构造"); }
    
    // 移动构造
    WithMove(WithMove&& other) noexcept : data_(other.data_) {
        LOG("WithMove 移动构造");
        other.data_ = nullptr;
    }
    
    // 移动赋值
    WithMove& operator=(WithMove&& other) noexcept {
        LOG("WithMove 移动赋值");
        if (this != &other) {
            delete data_;
            data_ = other.data_;
            other.data_ = nullptr;
        }
        return *this;
    }
    
    ~WithMove() {
        LOG("WithMove 析构");
        delete data_;
    }
    
    // 拷贝操作不会自动生成
    
private:
    int* data_;
};

/**
 * @brief Rule of Five 完整实现
 * 
 * 正确实现所有五个特殊成员函数
 */
class RuleOfFive {
public:
    // 1. 默认构造
    RuleOfFive() : data_(new int(0)) { LOG("RuleOfFive 默认构造"); }
    
    // 2. 析构函数
    ~RuleOfFive() {
        LOG("RuleOfFive 析构");
        delete data_;
    }
    
    // 3. 拷贝构造
    RuleOfFive(const RuleOfFive& other) : data_(new int(*other.data_)) {
        LOG("RuleOfFive 拷贝构造");
    }
    
    // 4. 拷贝赋值
    RuleOfFive& operator=(const RuleOfFive& other) {
        LOG("RuleOfFive 拷贝赋值");
        if (this != &other) {
            int* newData = new int(*other.data_);
            delete data_;
            data_ = newData;
        }
        return *this;
    }
    
    // 5. 移动构造
    RuleOfFive(RuleOfFive&& other) noexcept : data_(other.data_) {
        LOG("RuleOfFive 移动构造");
        other.data_ = nullptr;
    }
    
    // 6. 移动赋值
    RuleOfFive& operator=(RuleOfFive&& other) noexcept {
        LOG("RuleOfFive 移动赋值");
        if (this != &other) {
            delete data_;
            data_ = other.data_;
            other.data_ = nullptr;
        }
        return *this;
    }
    
    int value() const { return data_ ? *data_ : -1; }
    void setValue(int v) { if (data_) *data_ = v; }
    
private:
    int* data_;
};

/**
 * @brief Rule of Zero 最佳实践
 * 
 * 使用智能指针，让编译器自动生成所有操作
 */
class RuleOfZero {
public:
    explicit RuleOfZero(int value = 0) : data_(std::make_unique<int>(value)) {
        LOG("RuleOfZero 构造");
    }
    
    int value() const { return data_ ? *data_ : -1; }
    void setValue(int v) { if (data_) *data_ = v; }
    
    // 不需要声明任何特殊成员函数！
    // unique_ptr自动处理资源管理
    
private:
    std::unique_ptr<int> data_;
};

