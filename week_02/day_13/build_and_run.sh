#!/bin/bash

# Day 13 链表综合练习 - 编译运行脚本

set -e

echo "========================================"
echo "Day 13: 链表综合练习"
echo "========================================"

# 创建构建目录
BUILD_DIR="build"
if [ -d "$BUILD_DIR" ]; then
    echo "清理旧的构建目录..."
    rm -rf "$BUILD_DIR"
fi

mkdir -p "$BUILD_DIR"
cd "$BUILD_DIR"

# 配置和编译
echo ""
echo "[1/3] 配置 CMake..."
cmake ..

echo ""
echo "[2/3] 编译项目..."
make -j$(nproc)

echo ""
echo "[3/3] 运行程序..."
echo ""
echo "========================================"
echo "运行主程序 (day_13)"
echo "========================================"
./day_13

echo ""
echo "========================================"
echo "运行 LeetCode 160 测试"
echo "========================================"
./test_160_intersection

echo ""
echo "========================================"
echo "运行 LeetCode 148 测试"
echo "========================================"
./test_148_sort_list

echo ""
echo "========================================"
echo "所有测试通过!"
echo "========================================"
